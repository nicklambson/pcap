x = 16
while x > 0:
    print(f"before division: {x=}")
    # print("*", end='')
    print("*")
    x //= 2
    print(f"after division: {x=}")

# *****
# 1 // 2 is 0
# Integer division results in how many times the denominator will go into the numerator.
# The remainder is discarded.

# It's like division but dropping any decimal

